static HELLO: &[u8] = b"Hello World!";

/// Testing function
pub fn test() {
    let vga_buffer = 0x8000 as *mut u8;

    for (i, &byte) in HELLO.iter().enumerate() {
        unsafe {
            *vga_buffer.offset(i as isize * 2) = byte;
            *vga_buffer.offset(i as isize * 2 + 1) = 0xb;
        }
    }
}
